    public void setTotalPagar(int total){
        totalPagar = total;
        lTotal.setText("Total a pagar: $"+String.valueOf(totalPagar));
    }
    
    void CalculaCambio(int recibido){
        int total;
        if(recibido < totalPagar){
            lCambio.setText("Error: No puede ser menor al total");
            bEnviar.setEnabled(false);
        }else if(recibido >= totalPagar){
            total = recibido - totalPagar;
            lCambio.setText("Cambio: $"+total);
            bEnviar.setEnabled(true);
        }
    }
    
    void MostrarCampos(){
        if(cMetodoPago.getSelectedIndex() == 1){
            jLabel4.setVisible(false);
            cRecibido.setVisible(false);
            lCambio.setVisible(false);
            bEnviar.setEnabled(true);
        }else{
            jLabel4.setVisible(true);
            cRecibido.setVisible(true);
            lCambio.setVisible(true);
            CalculaCambio(Integer.parseInt(cRecibido.getText()));
            
        }
    }
