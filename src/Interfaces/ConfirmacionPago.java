    private void cRecibidoKeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_cRecibidoKeyReleased
        char c = evt.getKeyChar();
    
        // Permitir solo dígitos (y la tecla de borrar)
        if (!Character.isDigit(c) && c != KeyEvent.VK_BACK_SPACE) {
            evt.consume(); // Ignorar la entrada no válida
        }   

        // Validar si el campo no está vacío antes de convertirlo a número
        if (cRecibido.getText().length() > 0) {
            try {
                int recibido = Integer.parseInt(cRecibido.getText());
                CalculaCambio(recibido);
            } catch (NumberFormatException e) {
                // Manejo de error si ocurre un problema al convertir
                System.out.println("Entrada no válida.");
            }
        }
    }//GEN-LAST:event_cRecibidoKeyReleased

